#List methods
numbers = [5, 2, 1, 7, 4]#List
numbers2 = numbers.copy()#Copy of list
numbers.append(20)#add something in list
numbers.insert(3, 7)#add something in list at a specified position
numbers.remove(5)#remove something from list
numbers.pop()#remove last number
numbers.sort()#sort list
numbers.reverse()#reverse list order
print(numbers.count(7))#count number of 7s in list
print(50 in numbers)#find 50 in list
print(numbers)#print list
print(numbers2)#print duplicated list of original